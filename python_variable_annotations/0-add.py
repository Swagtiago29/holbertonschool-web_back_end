#!/usr/bin/env python3
"""
This module contains a function to add two floating-point numbers.

It demonstrates the use of type annotations in Python and provides
a simple example of module and function documentation.
"""


def add(a: float, b: float) -> float:
    """
    Add two floating-point numbers and return the result.

    Parameters:
    a (float): The first number to add.
    b (float): The second number to add.

    Returns:
    float: The sum of a and b.
    """
    return a + b
